var num1 =8;
var num2 = 5;

console.log("Value of Num 1 ="+num1)
console.log("Value of Num 2 ="+num2)

console.log("Value of Num 1 + Num 2 is:"+(num1+num2))    //addition
console.log("Value of Num 1 - Num 2 is:"+(num1-num2))    //substract
console.log("Value of Num 1 * Num 2 is:"+(num1*num2))    //multiply
console.log("Value of Num 1 ** Num 2 is:"+(num1**num2))  //exponential
console.log("Value of Num 1 / Num 2 is:"+(num1/num2))    //dividation

console.log("Value of num1 ++ is:"+(num1++))             //Use and then Increment
console.log(num1)
console.log("Value of ++num2 is:"+(++num2))              //first increment and the use
console.log(num2)

console.log("Value of num1-- is:"+(num1--))             //Use and then decrement
console.log(num1)
console.log("Value of --num2 is:"+(--num2))              //first decrement and the use
console.log(num2)



